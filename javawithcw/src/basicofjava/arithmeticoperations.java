package basicofjava;

public class arithmeticoperations {

    public static void main(String[] args) {
        int x=50;
        int y=30;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x% y  );

    }
}
