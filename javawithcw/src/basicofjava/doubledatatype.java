package basicofjava;
public class doubledatatype {
    public static void main(String[] args) {
        double x=5; //it represent 5 as 5.0
        double y=2; //it represent 2 as 2.0
        double z=x/y; //it 5.0/2.0=2.5
        System.out.println("if we devide "+x+" with "+y+" then we get :"+z);
        int a=7; //it represent as 7
        double b=2;  //it represent 2.0
        double c=a/b;  //it represent 7/2.0=3.5
        System.out.println("if we devide "+a+" with "+b+" then we get :"+c);
        int e=8; //it represent as 8
        int f=3;  //it represent 3
        double g=e/f;  //it represent 8/3=2.0
        System.out.println("if we devide "+e+" with "+f+" then we get :"+g);
    }
}
